package fcu.selab.progedu.data;

public class AssignmentScore {
  private int id = 0;

  private int auid = 0;

  private int score = -1;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public int getAuid() {
    return auid;
  }

  public void setAuid(int auid) {
    this.auid = auid;
  }

  public int getScore() {
    return score;
  }

  public void setScore(int score) {
    this.score = score;
  }
}
